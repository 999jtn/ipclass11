# A simple python program to find the average of three numbers.

fNo = float(input("Enter First Number: ")) #Gets 1st Number
sNo = float(input("Enter Second Number: ")) #Gets 2nd Number
tNo = float(input("Enter Third Number: ")) #Gets 3rd Number

avgNo = float((fNo + sNo + tNo)/3) #Formulae to calculate the avg of three Numbers.

print("") #Need some space, it's tough to breathe here.
print("Average of Three Numbers = " + str(avgNo)) #Print the Final asnwer